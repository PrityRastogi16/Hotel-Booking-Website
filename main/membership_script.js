
function visiblea(){
    var x = document.getElementById("aa");
    var y=document.getElementById("a");
  if (x.style.visibility === 'hidden') {
    x.style.visibility = 'visible';
    y.style.fontWeight=1000;
  } else {
    x.style.visibility = 'hidden';
    y.style.fontWeight=500;
  }
}

